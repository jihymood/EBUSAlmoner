package com.xpro.ebusalmoner.entity;

public class MessageBean {

	private String line;
	private String plateNumber;
	private String state;
	private String address;
	private String time;
	
	
	public MessageBean(String line, String plateNumber, String state,
			String address, String time) {
		super();
		this.line = line;
		this.plateNumber = plateNumber;
		this.state = state;
		this.address = address;
		this.time = time;
	}
	public String getLine() {
		return line;
	}
	public void setLine(String line) {
		this.line = line;
	}
	public String getPlateNumber() {
		return plateNumber;
	}
	public void setPlateNumber(String plateNumber) {
		this.plateNumber = plateNumber;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	@Override
	public String toString() {
		return "MessageBean [line=" + line + ", plateNumber=" + plateNumber
				+ ", state=" + state + ", address=" + address + ", time="
				+ time + "]";
	}
	
	
	
}
